package com.collegemanai.controller;

import com.collegemanai.dto.MessageDto;
import com.collegemanai.dto.mapper.MessageDtoMapper;
import com.collegemanai.exception.ChatException;
import com.collegemanai.exception.MessageException;
import com.collegemanai.exception.UserException;
import com.collegemanai.model.Message;
import com.collegemanai.model.User;
import com.collegemanai.request.SendMessageRequest;
import com.collegemanai.response.ApiResponse;
import com.collegemanai.services.MessageService;
import com.collegemanai.services.UserService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.*;

import java.util.List;

@RestController
@RequestMapping("/api/messages")
public class MessageController {

	@Autowired
	private MessageService messageService;
	
	@Autowired
	private UserService userService;
	
	@PostMapping
	public ResponseEntity<MessageDto> sendMessageHandler(
			@RequestHeader("Authorization")String jwt,  
			@RequestBody SendMessageRequest req) throws UserException, ChatException{
		
		User reqUser=userService.findUserProfileByJwt(jwt);
		
		req.setUserId(reqUser.getId());
		
		Message message=messageService.sendMessage(req);
		
		MessageDto messageDto=MessageDtoMapper.toMessageDto(message);
		
		return new ResponseEntity<>(messageDto,HttpStatus.OK);
	}
	
	@GetMapping("/chat/{chatId}")
	public ResponseEntity<List<MessageDto>> getChatsMessageHandler(@PathVariable Integer chatId) throws ChatException{
		
		List<Message> messages=messageService.getChatsMessages(chatId);
		
		List<MessageDto> messageDtos=MessageDtoMapper.toMessageDtos(messages);
		
		return new ResponseEntity<>(messageDtos, HttpStatus.ACCEPTED);
		
	}
	
	@DeleteMapping("/delete/{messageId}")
	public ResponseEntity<ApiResponse> deleteMessageHandler(@PathVariable Integer messageId) throws MessageException{
		
		messageService.deleteMessage(messageId);
		
		ApiResponse res=new ApiResponse("message deleted successfully",true);
		
		return new ResponseEntity<>(res, HttpStatus.ACCEPTED);
	}
}
